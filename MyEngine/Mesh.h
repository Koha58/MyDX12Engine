#pragma once
#include <vector>
#include <DirectXMath.h>

// =====================================================
// 頂点データ構造体
// -----------------------------------------------------
// ・3D モデルを構成する 1 頂点の情報をまとめる
// ・ここでは「位置」と「頂点カラー」のみを保持している
//   （通常は法線ベクトルや UV 座標なども含むが簡略化）
// =====================================================
struct Vertex
{
    DirectX::XMFLOAT3 Position;
    DirectX::XMFLOAT3 Normal;
    DirectX::XMFLOAT4 Color;
};

// =====================================================
// メッシュデータ構造体
// -----------------------------------------------------
// ・1 つの描画可能なメッシュ（形状）を表す
// ・頂点配列とインデックス配列を持つ
//   - Vertices: 頂点リスト（座標・色など）
//   - Indices : 頂点番号の並び。三角形を構成する順序を定義する
//
// 【例】立方体の場合
//   - Vertices に 8 頂点を定義
//   - Indices に「三角形を構成する 3 頂点の番号」を 12 個(=36要素)定義
// =====================================================
struct MeshData
{
    std::vector<Vertex> Vertices;       // 頂点リスト
    std::vector<unsigned int> Indices;  // インデックスリスト（三角形定義用）
};
